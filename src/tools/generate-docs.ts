// ─── Writer ──────────────────────────────────────────────────────────────────

async function writeDoc(docsDir: string, fileName: string, content: string): Promise<GeneratedDoc> {
  const filePath = join(docsDir, fileName);
  try {
    await writeFile(filePath, content, "utf-8");
    return { fileName, path: filePath, content, wasWritten: true };
  } catch (e) {
    return { fileName, path: filePath, content, wasWritten: false, error: String(e) };
  }
}

export async function generateOnboardingDocs(
  repoPath: string,
  analysis: RepoStructure
): Promise<GenerateDocsResult> {
  const repoName = repoPath.split("/").pop() ?? "this-repo";
  const docsDir = join(repoPath, "docs");

  await mkdir(docsDir, { recursive: true });

  const [codebase, workflow, domain, systemDesign] = await Promise.all([
    generateCodebaseMd(repoPath, analysis, repoName),
    generateWorkflowMd(repoPath, analysis, repoName),
    generateDomainMd(repoPath, analysis, repoName),
    generateSystemDesignMd(repoPath, analysis, repoName),
  ]);

  const docs = await Promise.all([
    writeDoc(docsDir, "codebase.md", codebase),
    writeDoc(docsDir, "workflow.md", workflow),
    writeDoc(docsDir, "domain.md", domain),
    writeDoc(docsDir, "system-design.md", systemDesign),
  ]);

  const written = docs.filter((d) => d.wasWritten).length;
  const failed = docs.filter((d) => !d.wasWritten);

  const summary = [
    `✅ Generated ${written}/4 onboarding docs in ${docsDir}`,
    written > 0 ? "Files written: " + docs.filter((d) => d.wasWritten).map((d) => d.fileName).join(", ") : "",
    failed.length > 0 ? "⚠️  Failed: " + failed.map((d) => `${d.fileName} (${d.error})`).join(", ") : "",
    "",
    "⚠️  These files are AUTO-GENERATED. Review them, correct anything wrong, and commit to your repo.",
    "The onboarding session will use them from /docs/ on subsequent runs.",
  ]
    .filter(Boolean)
    .join("\n");

  return { docs, docsDir, summary };
}

// ─── Freshness check ─────────────────────────────────────────────────────────

export interface DocsFreshnessResult {
  isStale: boolean;
  staleDocs: { fileName: string; ageInDays: number }[];
  summary: string;
}

export async function checkDocsFreshness(
  repoPath: string,
  staleDaysThreshold = 90
): Promise<DocsFreshnessResult> {
  const docsDir = join(repoPath, "docs");
  const files = ["codebase.md", "workflow.md", "domain.md", "system-design.md"];
  const staleDocs: { fileName: string; ageInDays: number }[] = [];
  const now = Date.now();

  for (const file of files) {
    try {
      const info = await stat(join(docsDir, file));
      const ageInDays = Math.floor((now - info.mtimeMs) / (1000 * 60 * 60 * 24));
      if (ageInDays > staleDaysThreshold) {
        staleDocs.push({ fileName: file, ageInDays });
      }
    } catch {
      // file doesn't exist — not stale, just missing
    }
  }

  const isStale = staleDocs.length > 0;
  const summary = isStale
    ? `⚠️  ${staleDocs.length} doc(s) are older than ${staleDaysThreshold} days:\n` +
      staleDocs.map((d) => `  - ${d.fileName} (${d.ageInDays} days old)`).join("\n") +
      "\n\nConsider running `/onboarding refresh-docs` to regenerate."
    : `✅ All docs are fresh (under ${staleDaysThreshold} days old).`;

  return { isStale, staleDocs, summary };
}
